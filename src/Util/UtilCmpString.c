/**
 * @brief       文字列比較
 * @details     pStr1とpStr2をnull文字またはn文字まで1文字づつ比較した結果を返
 *              す。
 *
 * @param[in]   *pStr1 文字列
 * @param[in]   *pStr2 文字列
 * @param[in]   length 比較最大文字数
 *
 * @return      最後に比較した文字の差(s1-s2)を返す。
 * @retval      0     同じ文字列
 * @retval      0以外 異なる文字列
 */
/******************************************************************************/
int MLibUtilCmpString( const char *pStr1,
                       const char *pStr2,
                       size_t     length  )
{
    /* 1文字毎に繰り返す */
    while ( ( *pStr1 == *pStr2 ) && ( length > 0 ) ) {
        /* null文字判定 */
        if ( *pStr1 == '\0' ) {
            /* null文字 */
            break;
        }

        pStr1++;
        pStr2++;
        length--;
    }

    return ( int ) ( *pStr1 - *pStr2 );
}


